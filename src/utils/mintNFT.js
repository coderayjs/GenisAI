export const mintNFT = async () => {
  try {
    if (!window.ethereum) {
      throw new Error('Please install MetaMask');
    }

    // Request account access
    await window.ethereum.request({ 
      method: 'eth_requestAccounts' 
    });

    // Create provider and signer
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const signer = provider.getSigner();
    
    const nftContract = new ethers.Contract(
      "0xc6F66A7154139C28604d61B5eCae05B38B391b0D",
      [
        "function mint() external",
        "function currentTokenId() view returns (uint256)"
      ],
      signer
    );

    // Add gas limit to transaction
    const tx = await nftContract.mint({
      gasLimit: 500000  // Adjust this value as needed
    });
    const receipt = await tx.wait();
    const tokenId = await nftContract.currentTokenId();

    return {
      success: true,
      transaction: receipt.transactionHash,
      tokenId: tokenId.toString()
    };

  } catch (error) {
    console.error('Minting error:', error);
    return {
      success: false,
      error: error.message
    };
  }
};

